import do
import numpy

###############################################################################
###############################################################################

class InverseCallable (object):

    def __call__ (self, *args: list) -> numpy.array:
        return 1.0 / numpy.array (args).flatten ()

    def __repr__ (self) -> str:
        return '1.0 / array ({0})'

###############################################################################
###############################################################################

if __name__ == "__main__":

    args = do.get_arguments ({
        'function': [[InverseCallable ()]]
    })

    try: do.loop (args.function, args.parameter_group, args.result,
        verbose=args.verbose)

    except KeyboardInterrupt:
        pass

